function validate()
{
  if (document.getElementById("username").value == "suman" &&
    document.getElementById("password").value == "suman1234")
  {
    alert("validation succeeded");
    
    location.href = "rn.html";
    
  }
  else
  {
    alert("validation failed");
    location.href = "fail.html";
  }
}
